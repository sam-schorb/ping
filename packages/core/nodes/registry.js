export function buildRegistryIndex(registry = NODE_REGISTRY) {
  const validationResult = validateRegistry(registry);

  if (!validationResult.ok) {
    throw createRegistryValidationError(validationResult);
  }

  return new Map(registry.map((definition) => [definition.type, definition]));
}

export function buildPalette(registry = NODE_REGISTRY) {
  return buildPaletteItems(registry);
}

export function resolveCanonicalNodeType(type) {
  return NODE_TYPE_ALIASES[type] ?? type;
}

export function getNodeDefinition(type, index = buildRegistryIndex()) {
  return index.get(resolveCanonicalNodeType(type));
}

