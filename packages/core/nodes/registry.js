function defineNode(definition) {
  return Object.freeze(definition);
}

function defineConstantNode(value) {
  return defineNode({
    type: `const${value}`,
    label: `Const ${value}`,
    description: `Overwrite the incoming value with the constant ${value}.`,
    category: "Constants",
    icon: `const${value}`,
    color: CATEGORY_COLORS.Constants,
    layout: "single-io",
    inputs: 1,
    outputs: 1,
    controlPorts: 0,
    hasParam: false,
    defaultParam: 1,
    onSignal: createConstantSignal(value),
    paletteOrder: 100 + value,
  });
}

function defineFilterNode({
  type,
  label,
  description,
  compare,
  paletteOrder,
  icon = type,
}) {
  return defineNode({
    type,
    label,
    description,
    category: "Logic",
    icon,
    color: CATEGORY_COLORS.Logic,
    layout: "single-io-control",
    inputs: 1,
    outputs: 1,
    controlPorts: 1,
    hasParam: true,
    defaultParam: 1,
    onControl: setParamFromControl,
    onSignal: createComparisonSignal(compare),
    paletteOrder,
  });
}

